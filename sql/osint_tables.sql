-- Create views for latest data with row timestamps exposed
-- METADATA$ROW_LAST_COMMIT_TIME shows when each row was committed to Snowflake
CREATE OR REPLACE VIEW DEMO.DEMO.OSINT_EARTHQUAKES_LATEST AS
SELECT 
    METADATA$ROW_LAST_COMMIT_TIME AS ROW_COMMIT_TIME,
    *
FROM DEMO.DEMO.OSINT_EARTHQUAKES
QUALIFY ROW_NUMBER() OVER (PARTITION BY ID ORDER BY INGESTED_AT DESC) = 1;

