-- Earthquake data from USGS API
CREATE TABLE IF NOT EXISTS DEMO.DEMO.OSINT_EARTHQUAKES (
    ID VARCHAR(100) NOT NULL,
    MAGNITUDE FLOAT,
    PLACE VARCHAR(500),
    LATITUDE FLOAT,
    LONGITUDE FLOAT,
    DEPTH FLOAT,
    EVENT_TIME TIMESTAMP_NTZ,
    TSUNAMI BOOLEAN,
    ALERT_LEVEL VARCHAR(50),
    SIGNIFICANCE INT,
    SOURCE VARCHAR(50) DEFAULT 'USGS',
    INGESTED_AT TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP(),
    PRIMARY KEY (ID, INGESTED_AT)
)
ROW_TIMESTAMP = TRUE;

-- Weather alerts from NWS API
CREATE TABLE IF NOT EXISTS DEMO.DEMO.OSINT_WEATHER_ALERTS (
    ID VARCHAR(200) NOT NULL,
    EVENT VARCHAR(200),
    SEVERITY VARCHAR(50),
    HEADLINE VARCHAR(1000),
    DESCRIPTION VARCHAR(8000),
    AREAS VARCHAR(4000),
    ONSET TIMESTAMP_NTZ,
    EXPIRES TIMESTAMP_NTZ,
    SOURCE VARCHAR(50) DEFAULT 'NWS',
    INGESTED_AT TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP(),
    PRIMARY KEY (ID, INGESTED_AT)
)
ROW_TIMESTAMP = TRUE;

-- Cryptocurrency prices from CoinGecko API
