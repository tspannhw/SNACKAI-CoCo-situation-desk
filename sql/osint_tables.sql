-- View for measuring pipeline latency (time from event to Snowflake commit)
CREATE OR REPLACE VIEW DEMO.DEMO.OSINT_PIPELINE_LATENCY AS
SELECT 
    'EARTHQUAKES' AS DATA_SOURCE,
    COUNT(*) AS RECORD_COUNT,
    AVG(TIMESTAMPDIFF('second', EVENT_TIME, METADATA$ROW_LAST_COMMIT_TIME)) AS AVG_LATENCY_SECONDS,
    MAX(TIMESTAMPDIFF('second', EVENT_TIME, METADATA$ROW_LAST_COMMIT_TIME)) AS MAX_LATENCY_SECONDS,
    MIN(METADATA$ROW_LAST_COMMIT_TIME) AS OLDEST_COMMIT,
    MAX(METADATA$ROW_LAST_COMMIT_TIME) AS NEWEST_COMMIT
FROM DEMO.DEMO.OSINT_EARTHQUAKES
WHERE METADATA$ROW_LAST_COMMIT_TIME IS NOT NULL
  AND EVENT_TIME IS NOT NULL
GROUP BY 1

UNION ALL

SELECT 
    'WEATHER_ALERTS' AS DATA_SOURCE,
    COUNT(*) AS RECORD_COUNT,
    AVG(TIMESTAMPDIFF('second', ONSET, METADATA$ROW_LAST_COMMIT_TIME)) AS AVG_LATENCY_SECONDS,
    MAX(TIMESTAMPDIFF('second', ONSET, METADATA$ROW_LAST_COMMIT_TIME)) AS MAX_LATENCY_SECONDS,
    MIN(METADATA$ROW_LAST_COMMIT_TIME) AS OLDEST_COMMIT,
    MAX(METADATA$ROW_LAST_COMMIT_TIME) AS NEWEST_COMMIT
FROM DEMO.DEMO.OSINT_WEATHER_ALERTS
WHERE METADATA$ROW_LAST_COMMIT_TIME IS NOT NULL
  AND ONSET IS NOT NULL
GROUP BY 1

UNION ALL

SELECT 
    'NEWS' AS DATA_SOURCE,
    COUNT(*) AS RECORD_COUNT,
    AVG(TIMESTAMPDIFF('second', PUBLISHED_AT, METADATA$ROW_LAST_COMMIT_TIME)) AS AVG_LATENCY_SECONDS,
    MAX(TIMESTAMPDIFF('second', PUBLISHED_AT, METADATA$ROW_LAST_COMMIT_TIME)) AS MAX_LATENCY_SECONDS,
    MIN(METADATA$ROW_LAST_COMMIT_TIME) AS OLDEST_COMMIT,
    MAX(METADATA$ROW_LAST_COMMIT_TIME) AS NEWEST_COMMIT
FROM DEMO.DEMO.OSINT_NEWS
WHERE METADATA$ROW_LAST_COMMIT_TIME IS NOT NULL
  AND PUBLISHED_AT IS NOT NULL
GROUP BY 1

UNION ALL

SELECT 
    'CRYPTO_PRICES' AS DATA_SOURCE,
    COUNT(*) AS RECORD_COUNT,
    NULL AS AVG_LATENCY_SECONDS,
    NULL AS MAX_LATENCY_SECONDS,
    MIN(METADATA$ROW_LAST_COMMIT_TIME) AS OLDEST_COMMIT,
    MAX(METADATA$ROW_LAST_COMMIT_TIME) AS NEWEST_COMMIT
FROM DEMO.DEMO.OSINT_CRYPTO_PRICES
WHERE METADATA$ROW_LAST_COMMIT_TIME IS NOT NULL
GROUP BY 1

UNION ALL

SELECT 
    'AIRCRAFT' AS DATA_SOURCE,
    COUNT(*) AS RECORD_COUNT,
    NULL AS AVG_LATENCY_SECONDS,
    NULL AS MAX_LATENCY_SECONDS,
    MIN(METADATA$ROW_LAST_COMMIT_TIME) AS OLDEST_COMMIT,
    MAX(METADATA$ROW_LAST_COMMIT_TIME) AS NEWEST_COMMIT
FROM DEMO.DEMO.OSINT_AIRCRAFT
WHERE METADATA$ROW_LAST_COMMIT_TIME IS NOT NULL
GROUP BY 1;

